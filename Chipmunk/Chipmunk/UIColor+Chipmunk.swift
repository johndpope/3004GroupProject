import Foundation
import UIKit

extension UIColor {
	
	static func appTintColor() -> UIColor {
		return UIColor(red: 1, green: 0.45, blue: 0.0039, alpha: 1)
	}
	
	static func dullGreyColor() -> UIColor {
		return UIColor(red: 0.1, green: 0.1, blue: 0.1, alpha: 1)
	}
	
}
